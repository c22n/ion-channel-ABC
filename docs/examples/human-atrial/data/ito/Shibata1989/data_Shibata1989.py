# Steady State Activation

def Act_Shibata():
    """
    Steady-State activation curve [Shibata1989]
    cf Fig 3a
    """
    x = np.arange(-30, 70, 10).tolist() + [80,]
    y = np.asarray([0.05186808768647655,
                    0.12676019804355798,
                    0.28385270591439526,
                    0.4992526352308727,
                    0.7266071584899667,
                    0.8298926453470465,
                    0.8973078656494293,
                    0.9423021428779867,
                    0.9693629080109075,
                    0.9949224588787261,
                    0.9922442666908765])
    ylower = np.asarray([0.04290295282036993,
                         0.05202047876738325,
                         0.27488757104828865,
                         0.48878952846308743,
                         0.7146493221839265,
                         0.7491772502812752,
                         0.8883394884198992,
                         0.9094148506729527,
                         0.9588998012431224,
                         0.9814698930344308,
                         0.9817779175596678])
    sem = np.abs(y-ylower)
    N = 3
    sd = np.sqrt(N)*sem
    return x, y.tolist(), sd.tolist()
